- (void)copyAddressBook:(ABAddressBookRef)addressBook{
    //获取联系人个数
    CFIndex numberOfPeoples = ABAddressBookGetPersonCount(addressBook);
    CFArrayRef peoples = ABAddressBookCopyArrayOfAllPeople(addressBook);
    NSLog(@"有%ld个联系人", numberOfPeoples);
    //循环获取联系人
    for (int i = 0; i < numberOfPeoples; i++) {
        ABRecordRef person = CFArrayGetValueAtIndex(peoples, i);
        AddressModel *linkMan = [[AddressModel alloc] init];
        linkMan.firstName = (__bridge NSString *)ABRecordCopyValue(person, kABPersonFirstNameProperty);
        linkMan.lastName = (__bridge NSString *)(ABRecordCopyValue(person, kABPersonLastNameProperty));
        linkMan.nickName = (__bridge NSString*)ABRecordCopyValue(person, kABPersonNicknameProperty);
        linkMan.organiztion = (__bridge NSString*)ABRecordCopyValue(person, kABPersonOrganizationProperty);
        linkMan.headImage = (__bridge NSData*)ABPersonCopyImageData(person);
        
        
        if (linkMan.firstName && linkMan.lastName) {
            linkMan.name = [NSString stringWithFormat:@"%@%@",linkMan.lastName, linkMan.firstName];
        }else if(!linkMan.firstName){
            linkMan.name = linkMan.lastName;
        }else{
            linkMan.name = linkMan.firstName;
        }
        if (!linkMan.name) {
            linkMan.name = linkMan.organiztion;
        }
        if (linkMan.nickName) {
            linkMan.name =[NSString stringWithFormat:@"%@", linkMan.nickName];
        }
        
        //读取电话多值
        NSMutableArray *phoneArray = [[NSMutableArray alloc] init];
        ABMultiValueRef phone = ABRecordCopyValue(person, kABPersonPhoneProperty);
        for (int k = 0; k<ABMultiValueGetCount(phone); k++)
        {
            //获取电话Label
            NSString * personPhoneLabel = (__bridge NSString*)ABAddressBookCopyLocalizedLabel(ABMultiValueCopyLabelAtIndex(phone, k));
            //获取該Label下的电话值
            NSString * tempstr = (__bridge NSString*)ABMultiValueCopyValueAtIndex(phone, k);
            NSArray *array = [NSArray arrayWithObjects:personPhoneLabel, tempstr, nil];
            [phoneArray addObject:array];
        }
        linkMan.phones = phoneArray;
        [_dataArray addObject:linkMan];
    }
    
    NSDictionary *dict = [self group:_dataArray  key:@"name"];
    
    _dataArray = [dict objectForKey:PinyinGroupResultKey];
    _headArray = [dict objectForKey:PinyinGroupCharKey];
    
    //回主线程刷UI
    dispatch_async(dispatch_get_main_queue(), ^{
        [self addView:self.view];
    });
    
    
    

}



/**
 获取汉字首字母
 */
-(NSDictionary *)group:(NSArray *)datas key:(NSString *)key{
    
    NSMutableArray *newSectionArray;
    UILocalizedIndexedCollation *collation = [UILocalizedIndexedCollation currentCollation];
    NSInteger sectionTitlesCount = [[collation sectionTitles] count];
    newSectionArray = [[NSMutableArray alloc] initWithCapacity:sectionTitlesCount];
    
    for (NSInteger index = 0; index < sectionTitlesCount; index ++) {
        NSMutableArray *array = [[NSMutableArray alloc] init];
        [newSectionArray addObject:array];
    }
    for (id model in datas) {
        //获取name属性的值所在的位置，比如"林丹"，首字母是L，在A~Z中排第11（第一位是0），sectionNumber就为11
        NSInteger sectionNumber = [collation sectionForObject:model collationStringSelector:NSSelectorFromString(key)];
        objc_setAssociatedObject(model, &PinyinGroupSectionNumberKey, @(sectionNumber), OBJC_ASSOCIATION_RETAIN_NONATOMIC);
    }
    
    NSMutableArray *charArray = [[NSMutableArray alloc] init];
    
    for (id model in datas) {
        NSInteger sectionNumber = [objc_getAssociatedObject(model, &PinyinGroupSectionNumberKey) integerValue];
        [newSectionArray[sectionNumber] addObject:model];
        unichar firstChar = [self firstCharactor:[[model valueForKey:key] description]];
        NSString *firstCharString = [[NSString stringWithFormat:@"%C",firstChar]uppercaseString];
        if (![charArray containsObject:firstCharString]) {
            [charArray addObject:firstCharString];
        }
    }
    
    NSArray *charSortArray = [charArray sortedArrayUsingSelector:@selector(compare:)];
    
    NSMutableArray *lastArray = [[NSMutableArray alloc] init];
    //对每个section中的数组按照name属性排序
    for (NSMutableArray *sectionArray in newSectionArray) {
        if (sectionArray.count != 0) {
            NSSortDescriptor* sortDescriptor = [[NSSortDescriptor alloc] initWithKey:key ascending:YES];
            NSArray* descriptors = [NSArray arrayWithObjects:sortDescriptor, nil];
            NSArray* sortedRoster = [sectionArray sortedArrayUsingDescriptors:descriptors];
            [lastArray addObject:[NSMutableArray arrayWithArray:sortedRoster]];
        }
    }
    NSArray *resultArray = [NSArray arrayWithArray:lastArray];
    
    return @{PinyinGroupCharKey:charSortArray,
             PinyinGroupResultKey:resultArray};
}

